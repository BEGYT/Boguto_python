# Задача 1: Дано вещественное число — цена 1 кг конфет. 
# Вывести стоимость 0.1, 0.2, ..., 1 кг конфет.

# Ввод цены за 1 кг конфет
while True:
    try:
        price_per_kg = float(input("Введите цену 1 кг конфет: "))
        if price_per_kg < 0:
            print("Цена должна быть положительной. Попробуйте еще раз.")
            continue
        break
    except ValueError:
        print("Неправильно ввели! Попробуйте еще раз.")

# Вывод стоимости от 0.1 до 1 кг
print("Стоимость конфет:")
for i in range(1, 11):  # от 1 до 10 (0.1 до 1.0)
    weight = i / 10  # перевод в кг
    cost = weight * price_per_kg
    print(f"{weight:.1f} кг: {cost:.2f} руб.")